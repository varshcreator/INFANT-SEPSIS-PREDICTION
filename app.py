@app.route('/predict', methods=['POST'])
def predict():
    try:
        u_name = session.get('user_name', 'Patient')
        u_email = session.get('receiver_email')

        input_data = [
            float(request.form['hr']),
            float(request.form['o2']),
            float(request.form['temp']),
            float(request.form['sbp']),
            float(request.form['resp']),
            float(request.form['ph']),
            float(request.form['wbc'])
        ]
        
        prediction = model.predict([input_data])[0]

        if int(prediction) == 1 and u_email:
            try:
                # Inga thaan message content-ai detail-aa sethuruken
                msg = Message("🔴 URGENT: SEPSIS RISK DETECTED - Stepsis AI", recipients=[u_email])
                
                msg.body = f"""
                EMERGENCY ALERT!
                
                Dear {u_name},
                
                Our AI system has analyzed the clinical vitals provided and detected a HIGH RISK of SEPSIS.
                
                Status: POSITIVE (High Risk)
                Action Required: 
                1. Contact emergency medical services immediately.
                2. Report to the nearest hospital for professional evaluation.
                3. Inform the healthcare provider about this AI-based sepsis screening result.
                
                Note: This is an automated alert generated by Stepsis AI for clinical monitoring purposes.
                
                Stay Safe,
                Sepsis Medical Team.
                """
                
                mail.send(msg)
                print(f"📧 Detailed Alert Sent to {u_email}")
            except Exception as mail_err:
                print(f"📧 Mail Error: {mail_err}")

        return render_template('result.html', prediction=int(prediction), name=u_name)
    except Exception as e:
        print(f"❌ Prediction Error: {e}")
        return f"System Error: {e}"

